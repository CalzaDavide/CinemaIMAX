package Data_tier;

public class Biglietto {
    int id;
    String nome;
    String cognome;
    int posto;
    Proiezione proiezione;

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public String getCognome() {
        return cognome;
    }

    public int getPosto() {
        return posto;
    }

    public Proiezione getProiezione() {
        return proiezione;
    }
}
